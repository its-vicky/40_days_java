package fiftydayscode_bran_state;

public class ContinueState {

	public static void main(String[] args) {
		for(int i=0;i<10;i++)
		{
			System.out.println("Learn Java..!!  :  "+i);
			if(i==7)
			{
				System.out.println("The Continue Statement..!!");
				continue;
			}
		}

	}

}
